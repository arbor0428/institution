<h3>중점연구소</h3>

<h4 class="m-45">중점연구소 설립 목적</h4>
<p>중점연구소는 학문 연구분야의 특성을 고려한 목적 지향적 연구소를 설립하여 대학의 전문적이며 특성화된 연구분야를 집중 육성하고자 함</p>

<h4 class="m-45">관련규정</h4>
<p>부설연구기관 설립 및 운영 규정</p>

