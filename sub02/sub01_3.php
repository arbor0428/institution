<h4 class="m-45">중점연구소 사업내용</h4>
<p class="dot_list">
	[과제선정] [연구과제] [처리중] 해당과제를 선택하여 수정 및 등록 함
</p>

<div class="pt_box m-20">
	<div class="box_inner">
		<p class="c1 bold2">1. 신청대상</p>
		<ul class="dash_list">
			<li>기존 중점연구소 및 일반연구소 새롭게 설립을 희망하는 본교 전임 교원</li>
		</ul>

		<p class="c1 bold2">2. 사업기간</p>
		<ul class="dash_list">
			<li>1년(단기), 2년(중기), 3년(장기)</li>
		</ul>

		<p class="c1 bold2">3. 지원규모 (교비지원)</p>
		<ul class="dash_list">
			<li>공 간 : 별도 공간배정 없음 (연구소장 연구실 활용)</li>
			<li>예 산 : 연구소별 차등 지원</li>
		</ul>

		<p class="c1 bold2">4. 선정절차</p>
		<ul class="dash_list">
			<li>신청인 대표가 산학협력단에 신청</li>
			<li>심사(1차/2차/3차) 후 총장 승인</li>
		</ul>

		<p class="c1 bold2">5. 유의사항</p>
		<ul class="dash_list">
			<li>연구소장의 임기는 1년으로 해당연도 3월 1일부터 차기연도 2월 28일까지로 함</li>
			<li>참여교수는 연구소 중복참여 허용 ( 중복참여시 연구소별 참여율을 기재 )</li>
		</ul>

		<p class="c1 bold2">6. 신청방법</p>
		<ul class="dash_list">
			<li>신청서류</li>
		</ul>
		<ul class="">
			<li>제출 공문 1부</li>
			<li>사업신청서 10부( A4용지 크기 좌철 책제본 ) 제출</li>
			<li>한글 파일로 작성한 서류 파일 제출</li>
		</ul>
		<ul class="dash_list">
			<li>신청기한 : 공고에 의한 신청기한 내</li>
			<li>접수방법 : 산학협력단 접수</li>
		</ul>
	</div>
</div>

