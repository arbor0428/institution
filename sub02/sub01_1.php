<h4 class="m-45">제출처</h4>
<p class="c1">연구관리시스템(n-KURIS)에서 과제 응모 시 업로드</p>

<h4 class="m-45">신청제한</h4>
<ul class="dash_list">
	<li>교내학술연구비를 지원 받고 연구종료 후 1년 내에 연구결과물을 제출하지 않은 경우 (단, 해당년도 교내학술연구비 포기신청서 제출자 제외, 논문게재예정증명서를 제출하는 경우 제외, 단 게재예정일은 결과물 제출 기한 이전까지)</li>
	<li>연구기간 중 6개월 이상 휴직 또는 장기출장(연구년 대상자는 제외)을 계획하고 있는 교원</li>
	<li>연구기간 내에 연구완료가 불가능하다고 인정되는 경우 또는 연구기간 도중에 퇴직하게 되는 경우</li>
	<li>본 연구비 지원과제에 연구책임자 또는 공동연구원으로 2과제 이상 참여하는 경우 한 과제만 지원</li>
	<li>연구년 종료 후 연구결과물을 제출하지 않은 교수</li>
	<li>직전학기 교내학술연구과제 수행 중인 교수</li>
</ul>

