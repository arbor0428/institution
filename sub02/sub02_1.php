<h4 class="m-45">연구비 중앙관리</h4>
<p>정부부처를 비롯한 각종 기관 및 사기업과 광운대학교산학협력단 간 계약 체결된 모든 연구과제는 연구비 입금에서 집행 및 정산에 이르기까지 모든 과정을 본교 연구관리스템인 <a class="c_red" href="http://nkuris.kw.ac.kr" title="nKURIS" target="_blank">nKURIS (http://nkuris.kw.ac.kr)</a>를 통하여 통합관리하고 있음</p>

<h4 class="m-45">1. 과제공모</h4>
<p>해당 기관 공모사항이나 산학협력단 홈페이지 최근공모사항 확인</p>

<h4 class="m-45">2. 과제신청</h4>
<p>과제계획서 및 제출서류를 준비하여 산학협력단을 경유하여 직인날인 후 해당기관으로 제출</p>

<h4 class="m-45">3. 과제계약</h4>
<p>
과제선정 통보를 받으면 해당기관의 계약 절차에 따라 서류를 준비하되, 산학협력단을 통하여 계약체결을 진행<br>
* 최종계획서는 협약담당자에 제출
</p>

