<h4 class="m-45">7. 연구결과물 및 연구비 사용실적보고서 제출</h4>
<p>연구책임자는 지원기관의 규정에 따라 연구결과물 및 연구비 사용실적보고서를 해당 기관의 양식에 맞게 작성하여 지원기관이 정해놓은 기한까지 제출해야 하며, 필요시 산학협력단 직인을 받은 후 공문과 함께 제출하여야 함</p>